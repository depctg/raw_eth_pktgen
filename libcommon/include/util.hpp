#ifndef _UTIL_HPP_
#define _UTIL_HPP_

#define _POSIX_C_SOURCE (199309L)
#define _GNU_SOURCE

#include <stdint.h>
#include <time.h>

static inline uint64_t microtime() {
    struct timespec ts;
    clock_gettime(CLOCK_REALTIME, &ts);
    uint64_t t = ts.tv_sec*1000*1000 + ts.tv_nsec/1000;
    return t;
}

#endif
